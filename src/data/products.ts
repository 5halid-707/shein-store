// Helper to build a 5-image gallery from a primary image + related ones.
const gallery = (primary: string, related: string[]) => {
  const g = [primary, ...related];
  while (g.length < 5) g.push(related[g.length % related.length] ?? primary);
  return g.slice(0, 5);
};

